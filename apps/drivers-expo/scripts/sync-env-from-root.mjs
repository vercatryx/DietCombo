/**
 * Writes apps/drivers-expo/.env by merging:
 *  - Repo root `.env` then `.env.local` (later overrides)
 *  - Only `NEXT_PUBLIC_*` keys → copied as `EXPO_PUBLIC_*` (safe for a client bundle)
 *  - Never copies secrets (service role, DB, JWT, SMTP, R2 secrets, etc.)
 *
 * Always sets EXPO_PUBLIC_API_BASE_URL=http://localhost:3000 so the app talks to your local Next dev server.
 * Override after sync if you need a LAN IP for a physical device.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const expoRoot = path.resolve(__dirname, '..');
const repoRoot = path.resolve(expoRoot, '..', '..');
const outPath = path.join(expoRoot, '.env');

const sources = [path.join(repoRoot, '.env'), path.join(repoRoot, '.env.local')];

/** @type {Map<string, string>} */
const merged = new Map();

function parseLine(line) {
    const t = line.trim();
    if (!t || t.startsWith('#')) return;
    const eq = t.indexOf('=');
    if (eq <= 0) return;
    const key = t.slice(0, eq).trim();
    let val = t.slice(eq + 1).trim();
    if (
        (val.startsWith('"') && val.endsWith('"')) ||
        (val.startsWith("'") && val.endsWith("'"))
    ) {
        val = val.slice(1, -1);
    }
    return { key, val };
}

for (const src of sources) {
    if (!fs.existsSync(src)) continue;
    const text = fs.readFileSync(src, 'utf8');
    for (const line of text.split('\n')) {
        const parsed = parseLine(line);
        if (!parsed) continue;
        const { key, val } = parsed;
        if (!key.startsWith('NEXT_PUBLIC_')) continue;
        const exKey = 'EXPO_PUBLIC_' + key.slice('NEXT_PUBLIC_'.length);
        merged.set(exKey, val);
    }
}

// Next.js dev server — proof uploads and API routes (required for this app).
merged.set('EXPO_PUBLIC_API_BASE_URL', 'http://localhost:3000');

if (!merged.has('EXPO_PUBLIC_APP_TIMEZONE')) {
    merged.set('EXPO_PUBLIC_APP_TIMEZONE', 'America/New_York');
}

function quoteIfNeeded(v) {
    if (/[\s#]/.test(v)) return `"${String(v).replace(/"/g, '\\"')}"`;
    return String(v);
}

const header = `# Generated by: npm run sync-env (apps/drivers-expo/scripts/sync-env-from-root.mjs)
# Source: repo root .env and .env.local — only NEXT_PUBLIC_* → EXPO_PUBLIC_* (no server secrets).
# API base points at local Next.js. Use your machine LAN IP instead of localhost on a physical phone.

`;

const lines = [...merged.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([k, v]) => `${k}=${quoteIfNeeded(v)}`);

fs.writeFileSync(outPath, header + lines.join('\n') + '\n', 'utf8');
console.log(`Wrote ${outPath} (${lines.length} vars)`);
